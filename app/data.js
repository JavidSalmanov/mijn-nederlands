function buildExercises(config, focusWords) {
  return {
    fillBlank: {
      prompt: config.fillPrompt,
      choices: config.fillChoices,
      answer: config.fillAnswer
    },
    match: {
      prompt: `Welke betekenis hoort bij '${config.matchWord}'?`,
      choices: [focusWords[config.matchWord].definition, ...config.matchChoices],
      answer: focusWords[config.matchWord].definition
    },
    context: {
      prompt: config.contextPrompt,
      choices: config.contextChoices,
      answer: config.contextAnswer
    }
  };
}

function createLesson(mode, config) {
  return {
    id: config.id,
    level: config.level,
    mode,
    theme: config.theme,
    title: config.title,
    intro: config.intro,
    sentences: config.sentences,
    focusWords: config.focusWords,
    exercises: buildExercises(config.exerciseConfig, config.focusWords)
  };
}

